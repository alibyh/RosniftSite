/**
 * Generates delivery rate values for all BEs, similar to the baseline in
 * src/deliveryCalculation.md (BE 1244). Outputs SQL to seed company_delivery_rates.
 *
 * Usage:
 *   node scripts/seed-company-delivery-rates.js [--exact] [--variation=0.03] [--output=file.sql]
 *
 *   --exact       Use baseline rates as-is for every BE (no random variation).
 *   --variation=N Apply ±N fraction to each rate (default 0.03 = ±3%).
 *   --output=FILE Write SQL to FILE; otherwise prints to stdout.
 *
 * Requires .env with VITE_SUPABASE_URL and VITE_SUPABASE_ANON_KEY.
 * Fetches distinct БЕ and "Наименование дочернего Общества" from inventory.
 */

const fs = require('fs');
const path = require('path');

// Baseline rates from deliveryCalculation.md (BE 1244)
// Weight 10-18: 50-250, 251-1000, 1001-2999, 3000+
// Over 18: same distance bands
const BASELINE = {
  rate_10_18_50_250: 23.03,
  rate_10_18_251_1000: 13.73,
  rate_10_18_1001_2999: 11.82,
  rate_10_18_3000plus: 11.76,
  rate_over18_50_250: 12.58,
  rate_over18_251_1000: 9.67,
  rate_over18_1001_2999: 8.92,
  rate_over18_3000plus: 8.89,
};

const RATE_KEYS = Object.keys(BASELINE);

function loadEnv() {
  const envPath = path.join(__dirname, '..', '.env');
  if (!fs.existsSync(envPath)) return {};
  const content = fs.readFileSync(envPath, 'utf8');
  const env = {};
  content.split('\n').forEach((line) => {
    const m = line.match(/^\s*([^#=]+)=(.*)$/);
    if (m) env[m[1].trim()] = m[2].trim().replace(/^["']|["']$/g, '');
  });
  return env;
}

function round2(n) {
  return Math.round(n * 100) / 100;
}

/** Apply ±variation to baseline; variation is a fraction (e.g. 0.03 = ±3%). */
function varyRates(baseline, variation) {
  const out = {};
  for (const key of RATE_KEYS) {
    const v = baseline[key];
    const delta = v * variation * (2 * Math.random() - 1);
    out[key] = round2(Math.max(0.01, v + delta));
  }
  return out;
}

function escapeSql(str) {
  if (str == null || str === '') return 'NULL';
  return "'" + String(str).replace(/'/g, "''") + "'";
}

function generateRow(be, companyName, rates) {
  const parts = [
    escapeSql(be),
    escapeSql(companyName || ''),
    rates.rate_10_18_50_250,
    rates.rate_10_18_251_1000,
    rates.rate_10_18_1001_2999,
    rates.rate_10_18_3000plus,
    rates.rate_over18_50_250,
    rates.rate_over18_251_1000,
    rates.rate_over18_1001_2999,
    rates.rate_over18_3000plus,
  ];
  return `  (${parts.join(', ')})`;
}

async function fetchDistinctBEs(supabaseUrl, anonKey) {
  const { createClient } = require('@supabase/supabase-js');
  const supabase = createClient(supabaseUrl, anonKey);
  const { data, error } = await supabase
    .from('inventory')
    .select('БЕ, "Наименование дочернего Общества"')
    .not('БЕ', 'is', null);

  if (error) throw new Error(`Supabase: ${error.message}`);

  const seen = new Map();
  (data || []).forEach((row) => {
    const be = (row['БЕ'] || '').trim();
    if (!be) return;
    const name = (row['Наименование дочернего Общества'] || '').trim();
    if (!seen.has(be)) seen.set(be, name);
  });
  return Array.from(seen.entries()).map(([be, companyName]) => ({ be, companyName }));
}

async function main() {
  const args = process.argv.slice(2);
  const exact = args.includes('--exact');
  let variation = 0.03;
  let outputFile = null;
  args.forEach((a) => {
    if (a.startsWith('--variation=')) variation = parseFloat(a.slice('--variation='.length), 10) || 0;
    if (a.startsWith('--output=')) outputFile = a.slice('--output='.length).trim() || null;
  });

  const env = { ...process.env, ...loadEnv() };
  const supabaseUrl = env.VITE_SUPABASE_URL || env.SUPABASE_URL;
  const anonKey = env.VITE_SUPABASE_ANON_KEY || env.SUPABASE_ANON_KEY;

  if (!supabaseUrl || !anonKey) {
    console.error('Missing VITE_SUPABASE_URL and VITE_SUPABASE_ANON_KEY in .env');
    process.exit(1);
  }

  console.error('Fetching distinct BEs from inventory...');
  const bes = await fetchDistinctBEs(supabaseUrl, anonKey);
  console.error(`Found ${bes.length} BEs.`);

  if (bes.length === 0) {
    console.error('No BEs found. Ensure inventory has rows with БЕ set.');
    process.exit(1);
  }

  const rows = bes.map(({ be, companyName }) => {
    const rates = exact ? { ...BASELINE } : varyRates(BASELINE, variation);
    return generateRow(be, companyName, rates);
  });

  const sql = `-- Generated by scripts/seed-company-delivery-rates.js
-- Baseline rates from src/deliveryCalculation.md (BE 1244); ${exact ? 'exact' : '±' + (variation * 100) + '% variation'}.

INSERT INTO company_delivery_rates (
  be, company_name,
  rate_10_18_50_250, rate_10_18_251_1000, rate_10_18_1001_2999, rate_10_18_3000plus,
  rate_over18_50_250, rate_over18_251_1000, rate_over18_1001_2999, rate_over18_3000plus,
  updated_at
)
VALUES
${rows.join(',\n')}
ON CONFLICT (be) DO UPDATE SET
  company_name = EXCLUDED.company_name,
  rate_10_18_50_250    = EXCLUDED.rate_10_18_50_250,
  rate_10_18_251_1000  = EXCLUDED.rate_10_18_251_1000,
  rate_10_18_1001_2999 = EXCLUDED.rate_10_18_1001_2999,
  rate_10_18_3000plus  = EXCLUDED.rate_10_18_3000plus,
  rate_over18_50_250    = EXCLUDED.rate_over18_50_250,
  rate_over18_251_1000  = EXCLUDED.rate_over18_251_1000,
  rate_over18_1001_2999 = EXCLUDED.rate_over18_1001_2999,
  rate_over18_3000plus  = EXCLUDED.rate_over18_3000plus,
  updated_at = now();
`;

  if (outputFile) {
    const outPath = path.isAbsolute(outputFile) ? outputFile : path.join(process.cwd(), outputFile);
    fs.writeFileSync(outPath, sql, 'utf8');
    console.error(`Wrote ${outPath}`);
  } else {
    console.log(sql);
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
